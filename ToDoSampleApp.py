# ToDo Sample Application Class

class ToDo:
    
    def __init__(self, title, date, time, description):
        self.title = title
        self.date = date
        self.time = time
        self.description = description
    
    def get_date(self):
        return self.date

    def get_time(self):
        return self.time
    
    def get_title(self):
        return self.title
    
    def get_description(self):
        return self.description

    

    
    